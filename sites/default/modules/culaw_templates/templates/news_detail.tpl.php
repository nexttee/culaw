					<div class="news-detail-wrap">
						<strong class="news-detail-wrap__tagline add">Nico Gurian ’17 won the prestigious Lawrence S. Greenbaum Prize for best oral presentation.</strong>
						<p>Four Columbia Law School students faced off over a thorny set of constitutional and procedural questions before a panel of federal appellate judges at the 2017 Paul, Weiss Harlan Fiske Stone Moot Court Competition finals on April 4.</p>
						<p>Charged with representing domestic violence survivors challenging a fictional governor’s decision to decriminalize behaviors such as battery and assault, Nico Gurian ’17 came away with the <a href="#">Lawrence S. Greenbaum Prize</a> for best oralist. Gurian’s co-counsel, Nicole E. Smith ’17, won the award for best written brief. On the opposing side, Jeremy C. Beutler ’17 and James Henseler ’17 represented the fictional governor.</p>
						<div class="news-detail-wrap__image-area">
							<img src="/<?php print drupal_get_path("module","culaw_templates");?>/images/img26.jpg" alt="image-description">
							<div class="news-detail-wrap__image-caption">
								<p>The final round of the Harlan Fiske Stone Moot Court Competition was held April 4 at the Law School. Pictured from left to right: finalists Jeremy C. Beutler ’17 and James Henseler ’17, Judge Mary Murguia, Judge Albert Diaz, Judge Patricia A. Millett, and finalists Nico Gurian ’17 and Nicole E. Smith ’17.</p>
							</div>
						</div>
						<p>The students argued before Judge Albert Diaz of the U.S. Court of Appeals for the 4th Circuit, Judge Mary H. Murguia of the U.S. Court of Appeals for the 9th Circuit, and <a href="#">Judge Patricia A. Millett</a> of the U.S. Court of Appeals for the D.C. Circuit, and in front of classmates, friends, family, and faculty members who came to Jerome Greene Hall to watch the prestigious annual appellate advocacy competition.</p>
						<strong class="news-detail-wrap__tagline">“You all were privileged to see four fantastic oral advocates,” said Millett, who asked some of the most challenging questions and joked afterward that family members in attendance might wonder why the bench was “so mean.”</strong>
						<p>The judges said the students did as well as some of the lawyers who appear before them in court and demonstrated the best of appellate advocacy, including: answering judges’ questions directly, conceding points when necessary, and pivoting back to their strongest arguments.</p>
						<div class="news-detail-wrap__image-area">
							<img src="/<?php print drupal_get_path("module","culaw_templates");?>/images/img27.jpg" alt="image-description">
							<div class="news-detail-wrap__image-caption">
								<p>The judges also praised the case—written by Angelica Tillander ’17 and Chris Helwig ’17—which centered on domestic violence survivors’ claims that the governor violated their due process rights by removing the threat of criminal charges for their abusers. A fictional lower court certified a class of survivors but then granted summary judgment in favor of the governor. The governor claimed to have acted in an effort to save prosecutorial resources and eliminate racial disparities in law enforcement.</p>
							</div>
						</div>
						<strong class="news-detail-wrap__tagline add1">“One side had the equities; the other side had the law,” Murguia said after the competition. “It was really quite a matchup.”</strong>
						<p>Greenbaum Prize winner Gurian said “the state put countless women in great danger…all the while restraining their ability to protect themselves.”</p>
						<p>But, later in the exchange, Millett countered that “the governor did what governors do”—attempt to make the best decision for the state as a whole.</p>
						<p>She also pressed the other side, which argued the women couldn’t statistically prove they were more likely to be abused in the future because of the law.</p>
						<p>“A 50 percent chance of a hangnail doesn’t seem so bad,” Millett began in a back-and-forth with Henseler. “But what if it were a 50 percent chance that you’d be murdered or that nuclear war would start? The fact that we’re talking about bodily violence… shouldn’t that be factored in… rather than the number-crunching you want us to do?”</p>
						<p>After the competition, best brief winner Smith said she was “incredibly grateful” for the experience and that the judges pushed the students “further than we had been pushed before.”</p>
						<p>Gurian added that “arguing before the panel was an exhilarating experience. I’m very proud to have participated in the finals alongside such talented oral advocates.”</p>
						<p>The annual competition is named in honor of Harlan Fiske Stone (Class of 1898), who was dean of Columbia Law School from 1910 until 1924 when President Calvin Coolidge appointed him U.S. attorney general. Stone was appointed to the U.S. Supreme Court the following year and became chief justice of the United States in 1941.</p>
						<p>Members of the 2016 moot court executive committee were: Harlan Fiske Stone Honors Moot Court Co-Directors Helwig and Tillander; Moot Court Program Executive Director VelaSusan Park ’17; Foundation Moot Court Program Director Elizabeth Marden ’17; and Specialized Moot Court Programs Director Kathryn Smits ’17. Professor Philip M. Genty is faculty director of the Harlan Fiske Stone Moot Court.</p>
						<p class="add">The Harlan Fiske Stone Moot Court Competition is made possible by the generous support of Paul, Weiss, Rifkind, Wharton &amp; Garrison.</p>
